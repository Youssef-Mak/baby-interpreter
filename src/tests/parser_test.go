func testLet(t *testing.T, s ast.Statement, name string) bool {
	if s.TokenLiteral() != "let" {
		t.Errorf("s.TokenLiteral not 'let'. got=%q", s.TokenLiteral())
		return false
	}
	letStmt, ok := s.(*ast.AssignmentStatement)
	if !ok {
		t.Errorf("s not *ast.LetStatement. got=%T", s)
		return false
	}
	if letStmt.Name.Value != name {
		t.Errorf("letStmt.Name.Value not '%s'. got=%s", name, letStmt.Name.Value)
		return false
	}
	if letStmt.Name.TokenLiteral() != name {
		t.Errorf("letStmt.Name.TokenLiteral() not '%s'. got=%s",
			name, letStmt.Name.TokenLiteral())
		return false
	}
	return true

}

func testIntegerLiteral(t *testing.T, exp ast.Expression, intVal int64) bool {
	literal, ok := exp.(*ast.IntegerLiteral)
	if !ok {
		t.Errorf("exp not *ast.IntegerLiteral. got=%T", exp)
		return false
	}
	if literal.Value != intVal {
		t.Errorf("literal.Value not %d. got=%d", intVal, literal.Value)
		return false
	}
	if literal.TokenLiteral() != fmt.Sprintf("%d", intVal) {
		t.Errorf("literal.TokenLiteral not %s. got=%s", fmt.Sprintf("%d", intVal),
			literal.TokenLiteral())
		return false
	}
	return true
}

